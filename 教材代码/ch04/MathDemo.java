//Math类的使用
public class MathDemo {
	public static void main(String[] args) {
		System.out.println(Math.E);// 输出e值
		System.out.println(Math.PI);// 输出PI值
		System.out.println(Math.exp(2));// 输出e平方
		System.out.println(Math.random());// 产生0~1随机数
		System.out.println(Math.sqrt(10.0));// 求10的平方根
		System.out.println(Math.pow(2, 3));// 计算2的3次方
		System.out.println(Math.round(99.4));// 四舍五入
		System.out.println(Math.abs(-8.88));// 求绝对值
	}
}
